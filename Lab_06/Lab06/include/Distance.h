#pragma once
#include "Haversine.h"
/**
 * This is a class representing distance
 */
class Distance{
public:

	/**
 	 * Distance constructor - calculating distance between given points
 	 * @param c1 - point on map
 	 * @param c2 - point on map
 	 */
	Distance(Coordinate c1, Coordinate c2): Distance(c1.Longitude(), c1.Latitude(),c2.Longitude(), c2.Latitude()){	}

	/**
 	 * Distance constructor - calculating distance between given points
 	 * @param x1 - given x
 	 * @param y1 - given y
 	 * @param x2 - given x
 	 * @param y2 - given y
 	 */
	Distance(double x1, double y1, double x2, double y2){
		_distance = Haversine(x1,y1,x2,y2);
	}

	/**
 	 * Getter of distance
 	 * @return value of distance
 	 */
	double Value(){
		return _distance;
	}

private:
	double _distance;
};